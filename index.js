// for in loop
// it will call keys of object

let obj = {
    ali: 20,
    sam: 35,
    ahmed: 43,
    nouman: 45
}
for(let a in obj){
    console.log(a)
}
// result will be
// ali
// sam
// ahmed
// nouman

